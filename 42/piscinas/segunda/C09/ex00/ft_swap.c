#include <unistd.h>

void	ft_putchar(char c);
void	ft_putstr(char *str);
int		ft_strlen(char *str);
int		ft_strcmp(char *s1, char *s2);

void	ft_swap(int *a, int *b)
{
	int	cambio;

	cambio = *a;
	*a = *b;
	*b = cambio;
}
